package com.ibm.xsp.extlib.designer.xspprops;

import java.net.URL;

import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.swt.graphics.Image;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "com.ibm.xsp.extlib.designer.xspprops"; //$NON-NLS-1$

	// The shared instance
	private static Activator plugin;
	
	/**
	 * The constructor
	 */
	public Activator() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}
	
    public static Image getImage(String location) {
        Image ret = getDefault().getImageRegistry().get( location );
        if (ret == null){
            ImageDescriptor imgDesc = getImageDescriptor( location );
            if (imgDesc != null) {
                ret = imgDesc.createImage();
                getDefault().getImageRegistry().put( location, ret );
            }
        }
        return ret;
    }
    
     public static ImageDescriptor getImageDescriptor(String location){
            String iconPath = "icons/"; //$NON-NLS-1$        
        URL imageUrl = getDefault().getBundle().getEntry(iconPath + location);   
        if (imageUrl != null) {
            return ImageDescriptor.createFromURL( imageUrl );
        }
        return null;
    }

}
